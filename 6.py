# ввод a, b, c
a = float(input())
b = float(input())
c = float(input())

# считаем дискриминант
D = (b ** 2) - (4 * a * c)
# если дискриминант меньше 0, вывод ''
if D < 0:
    print('')
# если дискриминант равен 0 (1 корень)
elif D == 0:
    print((-b) / (2 * a))
# иначе считаем по формуле нахождения корней
else:
    x1 = (-b + (D ** 0.5)) / (2 * a)
    x2 = (-b - (D ** 0.5)) / (2 * a)
    print(x1, x2)